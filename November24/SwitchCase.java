public class SwitchCase {
	public static void main (String[] args) {
	int tiffin = 2;
	switch(tiffin) {
	case 1: 
		System.out.println("idli");
	    break;
	case 2:
		System.out.println("dosa");
	    break;
	case 3:
		System.out.println("puri");
        break;
    case 4:
        	System.out.println("Upma");
             break;
     default:
    	 System.out.println("break");
	}
	}
}
	
